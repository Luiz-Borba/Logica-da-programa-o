print('Digite o valor da base do triângulo (em metros)')
base = float(input())
print('Digite o valor da altura do triângulo (em metros)')
altura = float(input())
area = (base*altura)
area = area/2
print(f'Obrigado! Portanto para um triângulo de base {base} (m) e altura de {altura}(m) a área do triângulo {area} (m²)')
